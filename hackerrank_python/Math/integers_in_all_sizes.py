# Integers Come In All Sizes
#
# Task
# Read four numbers, a, b, c, and d, and print the result of additon of a to the power of b and c to the power of d.
#
# Input Format
# Integers a, b, c, and d are given on four separate lines, respectively.
#
# Output Format
# Print the result of additon of a to the power of b and c to the power of d on one line.
#
# Solution
a = int(input());b = int(input())
c = int(input());d = int(input())
result = (pow(a,b) + pow(c,d))
print(result)
